package br.com.java.duvidas;

public class Igualdade {

	public static void main(String[] args) {
		// Atribuição
		int i = 2;
		String nome = "Jef";
				
		//
		// Comparador de igualdade
		if (i == 2) {
			System.out.println("i é igual a 2");

		} else {
			System.out.println("i não e igual a 2");
			
			//.equals (Comparador de igualdade entre String)
		}
		if (nome == "Jef") {
			System.out.println("E o Jef");
		} else {
			System.out.println("Não eo Jef");
		}
	}

}
